
public class TestaPontoFlutuante {
	
	public static void main(String[] args) {
		
		double salario = 1250.70; // mesmo que float
		salario = 1250.70; 
		
		System.out.println("Meu salário é: " + salario);
		
		double divisao = 3.14 / 2;
		System.out.println(divisao);
		
		int outraDivisao = 5 / 2;
		System.out.println(outraDivisao); // força o resultado em inteiro
		
		double novaTentativa = 5.0 / 2;
		System.out.println(novaTentativa); // um sendo double o resultado será com ponto flutuante
		
		
	}
}
