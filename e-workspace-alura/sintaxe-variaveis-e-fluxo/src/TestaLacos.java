
public class TestaLacos {
	public static void main(String[] args) {
		for(int multiplicador = 1; multiplicador <= 10; multiplicador++) {
			for(int i = 0; i <= 10; i++) {
				System.out.print(multiplicador * i);
				System.out.print(" ");
			}
			
			System.out.println();
		}
		
	}
}
